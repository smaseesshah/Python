# Numeric Data Types

# 1) integer
a = 5
print(type(a)) #printing datatype
print(a) 
a = -7
print(type(a)) #printing datatype
print(a)
a = 0
print(type(a)) #printing datatype
print(a)

# 2) float
b = 1.5
print(type(b)) #printing datatype
print(b)
b = 0.00006
print(type(b)) #printing datatype
print(b)
b = -593.8233
print(type(b)) #printing datatype
print(b)

# 3) Complex (a+bi:a,b=real and i=imaginary)
c = complex(1+2j)
print(type(c)) #printing datatype
print(c,"\n")

# Text Type : string
str = "Syed Muhammad Asees Shah"#must enclosed in cots
print(type(str)) #printing datatype
print(str,"\n")

# Boolean Type
bool = True
print(type(bool)) #printing datatype
print(bool)
bool = False
print(type(bool)) #printing datatype
print(bool,"\n")

# Sequenced Type

# 1) List
